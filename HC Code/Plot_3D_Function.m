%% Plotting Stuff for 3D Plots
function Plot_3D_Function(x, y, z, P)
    
    Font_Name   = 'Times New Roman';
    Font_Weight = 'bold';

    set(gcf, ...
        'position', [1000, 10, 2*400, 2*300], ...
        'Color',    'white')

    plot3(x, y, z, ...
        'Color',      P.Color, ...
        'LineStyle',  P.LineStyle, ...
        'MarkerSize', P.MarkerSize, ...
        'Marker',     P.Marker)
    
    % Set Default Tick Size
    if isfield(P, 'x_TSize') == 0
        P.x_TSize = 8;
    end

    set(gca, ...
        'FontName',   Font_Name, ...
        'FontWeight', Font_Weight, ...
        'FontSize',   P.x_TSize, ...
        'xGrid','On', ...
        'yGrid','On', ...
        'xLim',  P.x_Domain, ...
        'yLim',  P.y_Domain, ... 
        'zLim',  P.z_Domain, ... 
        'xTick', P.x_Domain(1): P.x_Tick_I: P.x_Domain(2), ...
        'yTick', P.y_Domain(1): P.y_Tick_I: P.y_Domain(2), ...
        'zTick', P.z_Domain(1): P.z_Tick_I: P.z_Domain(2))
    
    % Set Default Label Font Size
    if isfield(P, 'x_FSize') == 0
        P.x_FSize = 12;
        P.y_FSize = 12;
        P.z_FSize = 12;

    end

    if isfield(P, 'Title_FSize') == 0
        P.Title_FSize = 16;
    end
    
    title(P.Title, ...
        'FontName',   Font_Name, ...
        'FontWeight', Font_Weight, ...
        'FontSize',   P.Title_FSize)

    xlabel(P.x_Label, ...
        'FontName',   Font_Name, ...
        'FontWeight', Font_Weight, ...
        'FontSize',   P.x_FSize)

    ylabel(P.y_Label, ...
        'FontName',   Font_Name, ...
        'FontWeight', Font_Weight, ...
        'FontSize',   P.y_FSize)

    zlabel(P.z_Label, ...
        'FontName',   Font_Name, ...
        'FontWeight', Font_Weight, ...
        'FontSize',   P.z_FSize)

end